<?php

use Illuminate\Http\Resources\Json\Resource;

class ProductTypeResource extends Resource
{
    /**
     * Transform the resource into an array.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return array
     */
    public function toArray($request)
    {
        return [
            'name' => $this->getName()
        ];
    }
}
